#!/usr/bin/env python3
'''This module takes a string str1 and a string str2
as arguments and returns a concatenated string'''


def concat(str1: str, str2: str) -> str:
    '''Type-annoted function concatinate
    Arguments: str1(string), str2(string)
    returns the concatinated string of str1 and str2'''
    return (str1 + str2)
